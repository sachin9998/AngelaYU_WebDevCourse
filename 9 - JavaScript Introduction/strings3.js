
// slice(x,y) function in Strings

var fullName = "Sachin Narayan Alam";

// Using slice operation
var firstName = fullName.slice(0, 6); // char at 0 to char befor 6
var lastName = fullName.slice(15, 19); // char at 15 to char before 19

console.log(firstName); // Sachin
console.log(lastName); //  Alam